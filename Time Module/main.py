# Time Module in Python

import time

# time.time()
print()
print("time.time() : ", time.time())

# time.sleep()
print()
print(6)
# time.sleep(5)
print(11)

# time.localtime()
print()
print("time.localtime() : ", time.localtime())
print("time.localtime() : ", time.localtime()[2])


# time.strftime()
print()
print("time.strftime() Date: ", time.strftime("%Y:%m:%d"))
print("time.strftime() Date: ", time.strftime("%B"))
print("time.strftime() Time: ", time.strftime("%H:%M:%S"))
